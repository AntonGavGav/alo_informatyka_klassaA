public class User {
    private int age;
    private String city;
    private String day;

    public User(int age, String city, String day){
        this.age = age;
        this.city = city;
        this.day = day;
    }

    public int getAge() {
        return age;
    }

    public String getCity() {
        return city;
    }

    public String getDay() {
        return day;
    }

}
